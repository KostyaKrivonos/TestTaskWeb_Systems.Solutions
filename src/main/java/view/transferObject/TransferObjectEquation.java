package view.transferObject;

/**
 *
 * @author Алёшечка
 */
public class TransferObjectEquation {
    private double a, b, c;

    public TransferObjectEquation(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }
    
}
